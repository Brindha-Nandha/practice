package Integers;
/** 
 * 0 is even number
 * even or odd is applicable to negative numbers as well
 * 
 */
public class EvenOdd {

	public static void main(String[] args) {
		int n = 10;
		
		if(n%2==0) {
			System.out.println(n+" is an even number");
		}
		else {
			System.out.println(n+" is an odd number");
		}
	
		
	}

}
